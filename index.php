						<p>
							La diagonal [/dgnl] es el laboratorio de innovación de Real Plaza, la cadena de centros comerciales más grande del Perú. Nacimos en el contexto de profundos cambios en nuestra industria y país, producto de la entrada a una dinámica digital. Nuestro propósito es crear nuevos productos y servicios, y transformar la forma de trabajar dentro de la compañía. Y así impactar positivamente en la sociedad.

Somos un equipo apasionado y multidisciplinario. Venimos de distintos campos: la arquitectura, los negocios, el periodismo, la gestión, las artes, el diseño y las comunicaciones. Esta diversidad nos enriquece porque nos permite visibilizar nuestros sesgos y minimizarlos.

Trabajamos con una metodología centrada en las personas y sus necesidades –human centered design– de manera colaborativa. Nuestro trabajo se soporta en cinco valores: empatía, curiosidad, humildad, escepticismo, transparencia.
